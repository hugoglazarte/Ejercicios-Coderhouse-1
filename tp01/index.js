function sumar(){
	var suma1 = prompt('ingresa un nuevo numero');
	var suma2 = prompt('ingresa otro mas');
	var resultadofinal = parseInt(suma1) + parseInt(suma2);
	alert('el resultado de la Suma es :' + resultadofinal);
}

function restar(){
	var resta1 = prompt('ingresa un nuevo numero');
	var resta2 = prompt('ingresa otro mas');
	var resultadofinal = parseInt(resta1) - parseInt(resta2);
	alert('el resultado de la resta es :' + resultadofinal);
}

